void IPS114_Draw_Point(uint16 x, uint16 y, uint16 color)
{
    IPS114_set_region(x, y, x, y);
    IPS114_writedata_16bit(color);
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief      液晶显示字符
//  @param      x           坐标x方向的起点 参数范围 0 -（IPS114_X_MAX-1）
//  @param      y           坐标y方向的起点 参数范围 0 -（IPS114_Y_MAX/16-1）
//  @param      dat         需要显示的字符
//  @return     void
//  @since      v1.0
//  Sample usage:           IPS114_Char(0,0,'x');//坐标0,0写一个字符x
//-------------------------------------------------------------------------------------------------------------------
void IPS114_Char(uint16 x, uint16 y, const int8 dat, uint16 word_color, uint16 back_color)
{
    uint8 i, j;
    uint8 temp;

    for (i = 0; i < 16; i++)
    {
        IPS114_set_region(x, y + i, x + 7, y + i);
        temp = ASCII_8x16[dat - 32][i]; //减32因为是取模是从空格开始取得 空格在ascii中序号是32
        for (j = 0; j < 8; j++)
        {
            if (temp & 0x01)
            {
                IPS114_writedata_16bit(word_color);
            }
            else
                IPS114_writedata_16bit(back_color);
            temp >>= 1;
        }
    }
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief      液晶显示字符串
//  @param      x           坐标x方向的起点  参数范围 0 -（IPS114_X_MAX-1）
//  @param      y           坐标y方向的起点  参数范围 0 -（IPS114_Y_MAX-1）
//  @param      dat         需要显示的字符串
//  @return     void
//  @since      v1.0
//  Sample usage:           IPS114_String(0,0,"seekfree");
//-------------------------------------------------------------------------------------------------------------------
void IPS114_String(uint16 x, uint16 y, const int8 dat[], uint16 word_color, uint16 back_color)
{
    uint16 j;

    j = 0;
    while (dat[j] != '\0')
    {
        IPS114_Char(x + 8 * j, y, dat[j], word_color, back_color);
        j++;
    }
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief      总钻风(灰度摄像头)液晶显示函数
//  @param      *p          图像数组地址
//  @param      width       图像宽度
//  @param      height      图像高度
//  @return     void
//  @since      v1.0
//  Sample usage:           IPS114_IMAGE032(mt9v03x_csi_image[0], MT9V03X_CSI_W, MT9V03X_CSI_H)//显示灰度摄像头 图像
//  @note       图像的宽度如果超过液晶的宽度，则自动进行缩放显示。这样可以显示全视野
//-------------------------------------------------------------------------------------------------------------------
void IPS114_IMAGE032(uint8 *p, uint16 width, uint16 height)
{
    uint16 i, j;

    uint16 color = 0;
    uint16 temp = 0;

    uint16 coord_x = 0;
    uint16 coord_y = 0;

    coord_x = width > IPS114_X_MAX ? IPS114_X_MAX : width;
    coord_y = height > IPS114_Y_MAX ? IPS114_Y_MAX : height;
    IPS114_set_region(1, 1, coord_x, coord_y);

    for (j = 0; j < coord_y; j++)
    {
        for (i = 0; i < coord_x; i++)
        {
            temp = *(p + j * width + i * width / coord_x); //读取像素点
            color = (0x001f & ((temp) >> 3)) << 11;
            color = color | (((0x003f) & ((temp) >> 2)) << 5);
            color = color | (0x001f & ((temp) >> 3));
            IPS114_writedata_16bit(color);
        }
    }
}


void IPS114_IMAGE032_BIN(uint8 *p, uint8 threshold)
{
    uint16 i, j;

    uint16 temp = 0;

    IPS114_set_region(1, 1, IMAGE_W, IMAGE_H);

    for (j = 0; j < IMAGE_H; j++)
    {
        for (i = 0; i < IMAGE_W; i++)
        {
            temp = *(p + j * IMAGE_W + i);
            if (temp > threshold)
                IPS114_writedata_16bit(Color_WHITE);
            else
                IPS114_writedata_16bit(Color_BLACK);
        }
    }
}

void IPS114_IMAGE032_CON(uint16 *p, uint16 total)
{
    uint16 i, color[50];
    uint16 temp = IMAGE_H * IMAGE_W;
    uint32 seed;

    for (i = 0; (i < total) && (i < 50); i++)
    {
        seed = i + 100;
        color[i] = (uint16)IfxCpu_getRandomValueWithinRange(&seed, 0x0FFF, 0xFFF0);
    }

    IPS114_set_region(1, 1, IMAGE_W, IMAGE_H);

    for (i = 0; i < temp; i++)
    {
        if (p[i] == 0)
            IPS114_writedata_16bit(Color_BLACK);
        else if (p[i] == 1 && total == 1)
            IPS114_writedata_16bit(Color_WHITE);
        else if (p[i] <= 50)
            IPS114_writedata_16bit(color[p[i] - 1]);
        else
            IPS114_writedata_16bit(Color_WHITE);
    }
}


