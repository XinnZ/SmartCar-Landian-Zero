void IPS114_set_region(uint16 x1, uint16 y1, uint16 x2, uint16 y2)
{
#if (0 == IPS114_DISPLAY_DIR)
    IPS114_writeIndex(0x2a); //列地址设置
    IPS114_writedata_16bit(x1 + 52);
    IPS114_writedata_16bit(x2 + 52);
    IPS114_writeIndex(0x2b); //行地址设置
    IPS114_writedata_16bit(y1 + 40);
    IPS114_writedata_16bit(y2 + 40);
    IPS114_writeIndex(0x2c); //储存器写
#elif (1 == IPS114_DISPLAY_DIR)
    IPS114_writeIndex(0x2a); //列地址设置
    IPS114_writedata_16bit(x1 + 53);
    IPS114_writedata_16bit(x2 + 53);
    IPS114_writeIndex(0x2b); //行地址设置
    IPS114_writedata_16bit(y1 + 40);
    IPS114_writedata_16bit(y2 + 40);
    IPS114_writeIndex(0x2c); //储存器写
#elif (2 == IPS114_DISPLAY_DIR)
    IPS114_writeIndex(0x2a); //列地址设置
    IPS114_writedata_16bit(x1 + 40);
    IPS114_writedata_16bit(x2 + 40);
    IPS114_writeIndex(0x2b); //行地址设置
    IPS114_writedata_16bit(y1 + 53);
    IPS114_writedata_16bit(y2 + 53);
    IPS114_writeIndex(0x2c); //储存器写
#elif (3 == IPS114_DISPLAY_DIR)
    IPS114_writeIndex(0x2a); //列地址设置
    IPS114_writedata_16bit(x1 + 40);
    IPS114_writedata_16bit(x2 + 40);
    IPS114_writeIndex(0x2b); //行地址设置
    IPS114_writedata_16bit(y1 + 52);
    IPS114_writedata_16bit(y2 + 52);
    IPS114_writeIndex(0x2c); //储存器写
#endif
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief      液晶清屏函数
//  @param      color     		颜色设置
//  @return     void
//  @since      v1.0
//  Sample usage:               IPS114_Clear(YELLOW);// 全屏设置为黄色
//-------------------------------------------------------------------------------------------------------------------
void IPS114_Clear(uint16 color)
{
    uint16 i, j;
    IPS114_set_region(0, 0, IPS114_X_MAX - 1, IPS114_Y_MAX - 1);
    for (i = 0; i < IPS114_X_MAX; i++)
    {
        for (j = 0; j < IPS114_Y_MAX; j++)
        {
            IPS114_writedata_16bit(color);
        }
    }
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief      IPS114_Draw_Part 液晶画块
//  @param      xs           坐标x方向的起点
//  @param      ys           坐标x方向的起点
//  @param      xe           坐标y方向的终点
//  @param      ye           坐标y方向的终点
//  @param      color_dat    需要显示的颜色
//  @return     void
//  @since      v1.0
//  Sample usage:           IPS114_Draw_Point(0,0,RED);  //坐标0,0画一个红色的点
//-------------------------------------------------------------------------------------------------------------------
void IPS114_Draw_Part(uint16 xs, uint16 ys, uint16 xe, uint16 ye, uint16 color_dat)
{
    uint16 i, j;
    IPS114_set_region(xs, ys, xe, ye);
    for (i = 0; i < (xe - xs + 1); i++)
    {
        for (j = 0; j < (ye - ys + 1); j++)
        {
            IPS114_writedata_16bit(color_dat);
        }
    }
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief      IPS114_Draw_Rectangle 液晶画框
//  @param      xs           坐标x方向的起点
//  @param      ys           坐标x方向的起点
//  @param      xe           坐标y方向的终点
//  @param      ye           坐标y方向的终点
//  @param      color_dat    需要显示的颜色
//  @return     void
//  @since      v1.0
//  Sample usage:           IPS114_Draw_Point(0,0,RED);  //坐标0,0画一个红色的点
//-------------------------------------------------------------------------------------------------------------------
void IPS114_Draw_Rectangle(uint16 xs, uint16 ys, uint16 xe, uint16 ye, uint16 color_dat)
{
    IPS114_Draw_Part(xs, ys, xs, ye, color_dat); //画矩形左边
    IPS114_Draw_Part(xe, ys, xe, ye, color_dat); //画矩形右边
    IPS114_Draw_Part(xs, ys, xe, ys, color_dat); //画矩形上边
    IPS114_Draw_Part(xs, ye, xe, ye, color_dat); //画矩形下边
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief      液晶画点
//  @param      x           坐标x方向的起点
//  @param      y           坐标y方向的起点
//  @param      dat         需要显示的颜色
